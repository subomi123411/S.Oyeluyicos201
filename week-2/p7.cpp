#include <iostream>
using namespace std;

int main ()
{
    int x = 1;
    cout << "(x>0) is " << (x>0) << endl;
    cout << "(x<0) is " << (x<0) << endl;
    cout << "(x!=0) is " << (x!=0) << endl;
    cout << "(x>=0) is " << (x>=0) << endl;
    cout << "(x!=1) is " << (x!=1) << endl;

    return 0;
}